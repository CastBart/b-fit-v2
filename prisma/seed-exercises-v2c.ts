const BATCH_SIZE = 50

async function main() {
  // console.log('Starting exercise seed v2c (legs, core, abductors, adductors, functional)...')

  const existing = await prisma.exercise.findMany({ select: { name: true } })
  const existingNames = new Set(existing.map((e) => e.name))
  // console.log(`Found ${existingNames.size} existing exercises`)

  const allExercises: ExerciseSeed[] = [
    ...quadExercises,
    ...quadExercises2,
    ...hamstringExercises,
    ...hamstringExercises2,
    ...gluteExercises,
    ...calfExercises,
    ...calfExercises2,
    ...coreExercises,
    ...coreExercises2,
    ...abductorExercises,
    ...adductorExercises,
    ...functionalExercises,
    ...functionalExercises2,
    ...functionalExercises3,
  ]

  const toCreate = allExercises.filter((e) => !existingNames.has(e.name))
  // console.log(`Will create ${toCreate.length} new exercises (${allExercises.length - toCreate.length} already exist)`)

  // let created = 0
  for (let i = 0; i < toCreate.length; i += BATCH_SIZE) {
    const batch = toCreate.slice(i, i + BATCH_SIZE)
    // const result = await prisma.exercise.createMany({ data: batch })
    await prisma.exercise.createMany({ data: batch })
    // created += result.count
    // console.log(`Batch ${Math.floor(i / BATCH_SIZE) + 1}: inserted ${result.count}`)
  }

  // console.log(`Done! Created ${created} exercises.`)
}

main()
  .catch((e) => {
    console.error(e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })
